    public static void summarizeMemory(CreeperMother girl) {
        // 异步执行，不卡游戏主线程
        CompletableFuture.runAsync(() -> {
            try {
                // 1. 构建请求体
                JsonObject json = new JsonObject();
                json.addProperty("model", AIConfig.getModel());

                JsonArray messages = new JsonArray();

                // 设定一个专门负责总结的 System Prompt
                JsonObject systemMsg = new JsonObject();
                systemMsg.addProperty("role", "system");
                systemMsg.addProperty("content", "你是一个记忆整理助手。请将以下对话历史总结成一段简短的背景描述（100字以内）。" +
                        "你要保留重要的事实（如玩家的名字、喜好、承诺过的事情），删除无意义的寒暄。总结后的文字将作为 AI 的长期记忆。");
                messages.add(systemMsg);

                // 把当前老婆的所有记忆塞进去让 AI 总结
                messages.addAll(girl.getChatMessages());
                json.add("messages", messages);

                // 2. 发送请求 (这里假设你已经封装好了具体的 HTTP 发送逻辑)
                // 这里用伪代码代表你的请求过程
                String responseBody = sendHttpPost(AIConfig.getApiUrl(), json.toString());

                // 3. 解析响应内容
                JsonObject responseJson = JsonParser.parseString(responseBody).getAsJsonObject();
                String summaryResult = responseJson.get("choices").getAsJsonArray()
                        .get(0).getAsJsonObject()
                        .get("message").getAsJsonObject()
                        .get("content").getAsString();

                // 4. 回到服务器线程更新记忆
                girl.getServer().execute(() -> {
                    JsonArray newMemory = new JsonArray();
                    JsonObject summaryObj = new JsonObject();
                    summaryObj.addProperty("role", "system");
                    summaryObj.addProperty("content", "【前情提要】： " + summaryResult);

                    newMemory.add(summaryObj);

                    // 将老婆的记忆替换为这一条“脱水”后的精华摘要
                    girl.updateMemory(newMemory);
                    System.out.println("[AI记忆管理] 已成功压缩 " + girl.getName().getString() + " 的记忆。");
                });

            } catch (Exception e) {
                System.err.println("记忆总结失败: " + e.getMessage());
                e.printStackTrace();
            }
        });
    }

